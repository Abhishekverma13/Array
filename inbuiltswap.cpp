#include<iostream>
using namespace std;

int main(){
    int a = 5, b = 6;
    cout<<"Before swapping -: "<<endl;
    cout<<"a = "<<a<<endl;
    cout<<"b = "<<b<<endl;

    swap(a,b);

    cout<<"After  swapping -: "<<endl;
    cout<<"a = "<<a<<endl;
    cout<<"b = "<<b<<endl;

    return 0;



}